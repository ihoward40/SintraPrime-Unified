class WindowsDeployer:
    """Orchestrates generation of all Windows deployment artifacts."""

    def __init__(self, app_config: Optional[AppConfig] = None):
        self.app_config = app_config or AppConfig()
        self._ps_gen = PowerShellSetupGenerator()
        self._spec_gen = PyInstallerSpecGenerator()
        self._nsis_gen = NSISInstallerGenerator()
        self._updater_gen = AutoUpdaterConfigGenerator()
        self._tray_gen = SystemTrayConfigGenerator()
        self._reg_gen = RegistryManifestGenerator()

    def generate_bundle(self) -> DeploymentBundle:
        """Generate the complete deployment bundle."""
        cfg = self.app_config
        bundle = DeploymentBundle(
            bundle_id=f"bundle_{uuid.uuid4().hex[:8]}",
            app_config=cfg,
        )

        bundle.artifacts.append(self._ps_gen.generate(cfg))
        bundle.artifacts.append(self._spec_gen.generate(cfg))
        bundle.artifacts.append(self._nsis_gen.generate(cfg))
        if cfg.enable_auto_update:
            bundle.artifacts.append(self._updater_gen.generate(cfg))
        if cfg.enable_system_tray:
            bundle.artifacts.append(self._tray_gen.generate(cfg))
        bundle.artifacts.append(self._reg_gen.generate(cfg))

        return bundle

    def generate_artifact(self, artifact_type: str) -> Optional[DeploymentArtifact]:
        """Generate a single artifact by type."""
        cfg = self.app_config
        generators = {
            "powershell_setup": self._ps_gen,
            "pyinstaller_spec": self._spec_gen,
            "nsis_installer": self._nsis_gen,
            "autoupdater_config": self._updater_gen,
            "system_tray_config": self._tray_gen,
            "registry_manifest": self._reg_gen,
        }
        gen = generators.get(artifact_type)
        return gen.generate(cfg) if gen else None

    def validate_bundle(self, bundle: DeploymentBundle) -> Dict[str, Any]:
        """Validate that all required artifacts are present and non-empty."""
        required = ["powershell_setup", "pyinstaller_spec", "nsis_installer", "registry_manifest"]
        missing = [r for r in required if r not in bundle.artifact_types()]
        empty = [a.artifact_type for a in bundle.artifacts if not a.content.strip()]

        return {
            "valid": len(missing) == 0 and len(empty) == 0,
            "missing_artifacts": missing,
            "empty_artifacts": empty,
            "total_artifacts": len(bundle.artifacts),
            "total_size_bytes": bundle.total_size_bytes(),
        }
