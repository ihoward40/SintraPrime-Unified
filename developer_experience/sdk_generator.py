# Python reserved keywords that cannot be used as parameter names
PYTHON_RESERVED_KEYWORDS = {
    "False", "None", "True", "and", "as", "assert", "async", "await",
    "break", "class", "continue", "def", "del", "elif", "else", "except",
    "finally", "for", "from", "global", "if", "import", "in", "is",
    "lambda", "nonlocal", "not", "or", "pass", "raise", "return", "try",
    "while", "with", "yield",
}


# ---------------------------------------------------------------------------
# Type mapping helpers
# ---------------------------------------------------------------------------

PYTHON_TYPE_MAP = {
    "string": "str",
    "integer": "int",
    "number": "float",
    "boolean": "bool",
    "array": "list",
    "object": "dict",
}

TS_TYPE_MAP = {
    "string": "string",
    "integer": "number",
    "number": "number",
    "boolean": "boolean",
    "array": "any[]",
    "object": "Record<string, any>",
}


def to_snake_case(name: str) -> str:
    """Convert camelCase or PascalCase to snake_case."""
    s1 = re.sub(r"(.)([A-Z][a-z]+)", r"\1_\2", name)
    return re.sub(r"([a-z0-9])([A-Z])", r"\1_\2", s1).lower()


def to_camel_case(name: str) -> str:
    """Convert snake_case to camelCase."""
    parts = name.split("_")
    return parts[0] + "".join(p.capitalize() for p in parts[1:])


def to_pascal_case(name: str) -> str:
    """Convert snake_case or camelCase to PascalCase."""
    s = to_snake_case(name)
    return "".join(p.capitalize() for p in s.split("_"))


def get_python_type(schema: dict, schemas: dict) -> str:
    """Resolve OpenAPI schema to Python type hint."""
    if "$ref" in schema:
        ref_name = schema["$ref"].split("/")[-1]
        return ref_name
    schema_type = schema.get("type", "object")
    if schema_type == "array":
        items = schema.get("items", {})
        inner = get_python_type(items, schemas)
        return f"list[{inner}]"
    return PYTHON_TYPE_MAP.get(schema_type, "Any")


def get_ts_type(schema: dict, schemas: dict) -> str:
    """Resolve OpenAPI schema to TypeScript type."""
    if "$ref" in schema:
        ref_name = schema["$ref"].split("/")[-1]
        return ref_name
    schema_type = schema.get("type", "object")
    if schema_type == "array":
        items = schema.get("items", {})
        inner = get_ts_type(items, schemas)
        return f"{inner}[]"
    return TS_TYPE_MAP.get(schema_type, "any")


# ---------------------------------------------------------------------------
# OpenAPI spec loader
