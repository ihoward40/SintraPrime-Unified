class BuildScriptGenerator:
    """Generates the build.bat / build.sh script."""

    def __init__(self, config: ExeConfig):
        self.config = config

    def generate_bat(self) -> str:
        c = self.config
        return textwrap.dedent(f"""\
            @echo off
            REM SintraPrime Windows Build Script v{c.app_version}
            REM Auto-generated by ExeBuilder

            echo Building {c.app_name} v{c.app_version}...

            REM Install dependencies
            pip install pyinstaller pystray pillow --quiet

            REM Clean previous builds
            if exist "{c.build_dir}" rmdir /s /q "{c.build_dir}"
            if exist "{c.output_dir}\\{c.app_name}.exe" del /f "{c.output_dir}\\{c.app_name}.exe"

            REM Build executable
            pyinstaller {c.app_name}.spec --clean --noconfirm

            if %ERRORLEVEL% neq 0 (
                echo BUILD FAILED
                exit /b 1
            )

            echo Build successful: {c.output_dir}\\{c.app_name}.exe

            REM Build installer (requires NSIS)
            where makensis >nul 2>&1
            if %ERRORLEVEL% equ 0 (
                echo Building installer...
                if not exist "{c.nsis_output_dir}" mkdir "{c.nsis_output_dir}"
                makensis {c.app_name}_installer.nsi
                echo Installer: {c.nsis_output_dir}\\{c.app_name}_Setup_v{c.app_version}.exe
            ) else (
                echo NSIS not found — skipping installer build
            )

            echo Done!
        """)

    def generate_sh(self) -> str:
        c = self.config
        return textwrap.dedent(f"""\
            #!/bin/bash
            # SintraPrime Build Script (Linux/macOS CI) v{c.app_version}
            set -e

            echo "Building {c.app_name} v{c.app_version}..."

            pip install pyinstaller pystray pillow --quiet

            rm -rf "{c.build_dir}" "{c.output_dir}/{c.app_name}"

            pyinstaller {c.app_name}.spec --clean --noconfirm

            echo "Build complete: {c.output_dir}/{c.app_name}"
        """)


# ---------------------------------------------------------------------------
# Main ExeBuilder orchestrator
# ---------------------------------------------------------------------------

class ExeBuilder:
    """
    Orchestrates generation of all Windows deployment artifacts:
    - PyInstaller .spec file
    - version_info.txt
    - NSIS installer script
    - Tray icon integration script
    - Build scripts (bat + sh)
    - build_config.json
    """

    def __init__(self, config: Optional[ExeConfig] = None, output_dir: Optional[str] = None):
        self.config = config or ExeConfig()
        if output_dir:
            self.config.output_dir = output_dir
        self._spec_gen = SpecGenerator(self.config)
        self._nsis_gen = NSISGenerator(self.config)
        self._tray_gen = TrayIconGenerator(self.config)
        self._build_gen = BuildScriptGenerator(self.config)
        self._generated_files: List[str] = []

    def generate_all(self, base_dir: str = ".") -> Dict[str, str]:
        """
        Generate all deployment artifacts into base_dir.
        Returns a dict of {filename: content}.
        """
        base = Path(base_dir)
        base.mkdir(parents=True, exist_ok=True)

        artifacts: Dict[str, str] = {
            f"{self.config.app_name}.spec": self._spec_gen.generate(),
            "version_info.txt": self._spec_gen.generate_version_info(),
            f"{self.config.app_name}_installer.nsi": self._nsis_gen.generate(),
            "tray_icon.py": self._tray_gen.generate(),
            "build.bat": self._build_gen.generate_bat(),
            "build.sh": self._build_gen.generate_sh(),
            "build_config.json": json.dumps(self.config.to_dict(), indent=2),
        }

        for filename, content in artifacts.items():
            path = base / filename
            path.write_text(content, encoding="utf-8")
            self._generated_files.append(str(path))

        return artifacts

    def get_generated_files(self) -> List[str]:
        return list(self._generated_files)

    def validate_config(self) -> List[str]:
        """Returns list of validation warnings."""
        warnings: List[str] = []
        if not self.config.main_script.endswith(".py"):
            warnings.append("main_script should end with .py")
        if not self.config.icon_path.endswith((".ico", ".png")):
            warnings.append("icon_path should be .ico or .png")
        if self.config.port < 1024 or self.config.port > 65535:
            warnings.append(f"port {self.config.port} is outside recommended range 1024-65535")
        if self.config.app_version.count(".") < 2:
            warnings.append("app_version should be in X.Y.Z format")
        return warnings

    def build(self, cwd: Optional[str] = None) -> subprocess.CompletedProcess:
        """
        Runs PyInstaller with the generated spec file.
        Only works on the host OS where PyInstaller is installed.
        """
        spec_file = f"{self.config.app_name}.spec"
        cmd = [sys.executable, "-m", "PyInstaller", spec_file, "--clean", "--noconfirm"]
        return subprocess.run(
            cmd,
            cwd=cwd or ".",
            capture_output=True,
            text=True,
        )

    @classmethod
    def from_json(cls, json_path: str) -> "ExeBuilder":
        with open(json_path, encoding="utf-8") as f:
            data = json.load(f)
        config = ExeConfig(**{k: v for k, v in data.items() if hasattr(ExeConfig, k)})
        return cls(config)
