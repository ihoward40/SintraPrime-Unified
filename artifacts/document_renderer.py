class _Formatter:
    """Internal helper for text formatting."""

    @staticmethod
    def horizontal_rule(width: int = 80, char: str = "═") -> str:
        return char * width

    @staticmethod
    def center(text: str, width: int = 80, fill: str = " ") -> str:
        return text.center(width, fill)

    @staticmethod
    def box(lines: List[str], width: int = 80) -> str:
        inner_w = width - 4
        result = ["╔" + "═" * (width - 2) + "╗"]
        for line in lines:
            padded = line[:inner_w].ljust(inner_w)
            result.append("║  " + padded + "  ║")
        result.append("╚" + "═" * (width - 2) + "╝")
        return "\n".join(result)

    @staticmethod
    def section_header(number: str, title: str, width: int = 80) -> str:
        label = f"{number}. {title.upper()}"
        return f"\n{label}\n{'─' * min(len(label) + 2, width)}\n"

    @staticmethod
    def wrap(text: str, width: int = 76, indent: str = "") -> str:
        return textwrap.fill(text, width=width, initial_indent=indent,
                             subsequent_indent=indent)

    @staticmethod
    def format_currency(amount) -> str:
        try:
            return f"${float(amount):>14,.2f}"
        except (TypeError, ValueError):
            return f"{'[AMOUNT]':>15}"

    @staticmethod
    def format_date(dt=None) -> str:
        if dt is None:
            dt = datetime.now()
        return dt.strftime("%B %d, %Y")

    @staticmethod
    def page_break() -> str:
        return "\n\f\n"  # Form-feed character

    @staticmethod
    def numbered_list(items: List[str], start: int = 1) -> str:
        lines = []
        for i, item in enumerate(items, start):
            lines.append(f"  {i:>2}. {item}")
        return "\n".join(lines)

    @staticmethod
    def alpha_list(items: List[str]) -> str:
        alpha = "abcdefghijklmnopqrstuvwxyz"
        lines = []
        for i, item in enumerate(items):
            lines.append(f"      ({alpha[i]}) {item}")
        return "\n".join(lines)

    @staticmethod
    def roman_section(number: int, title: str) -> str:
        numerals = ["I", "II", "III", "IV", "V", "VI", "VII", "VIII", "IX", "X",
                    "XI", "XII", "XIII", "XIV", "XV", "XVI", "XVII", "XVIII", "XIX", "XX"]
        n = numerals[number - 1] if number <= len(numerals) else str(number)
        return f"\n{n}. {title.upper()}"

    @staticmethod
    def text_to_html(text: str, title: str, style: DocumentStyle) -> str:
        escaped = (text
                   .replace("&", "&amp;")
                   .replace("<", "&lt;")
                   .replace(">", "&gt;"))
        html_body = escaped.replace("\n", "<br>\n")
        return f"""<!DOCTYPE html>
<html lang="en">
<head>
  <meta charset="UTF-8">
  <title>{title}</title>
  <style>
    body {{
      font-family: {style.font_family}, serif;
      color: {style.primary_color};
      max-width: 900px;
      margin: 40px auto;
      padding: 40px;
      line-height: 1.6;
    }}
    h1, h2, h3 {{ color: {style.primary_color}; border-bottom: 2px solid {style.accent_color}; }}
    .footer {{ color: #888; font-size: 0.85em; border-top: 1px solid {style.accent_color}; margin-top: 40px; padding-top: 10px; }}
    pre {{ font-family: monospace; white-space: pre-wrap; }}
  </style>
</head>
<body>
  <pre>{html_body}</pre>
  <div class="footer">{style.footer_text}</div>
</body>
</html>"""

    @staticmethod
    def text_to_markdown(text: str, title: str) -> str:
        return f"# {title}\n\n```\n{text}\n```\n"


_fmt = _Formatter()


