    def _render_financial_table(self, tbl: Dict[str, Any], width: int) -> str:
        headers = tbl.get("headers", ["Description", "Amount"])
        rows = tbl.get("rows", [])
        totals = tbl.get("totals", {})

        n_cols = len(headers)
        col_w = (width - 4) // n_cols

        def row_str(cells: List[str]) -> str:
            parts = []
            for c in cells:
                parts.append(str(c)[:col_w - 2].ljust(col_w - 2))
            return "  │ " + " │ ".join(parts) + " │"

        lines = []
        sep = "  ├" + "┼".join(["─" * col_w] * n_cols) + "┤"
        top = "  ┌" + "┬".join(["─" * col_w] * n_cols) + "┐"
        bot = "  └" + "┴".join(["─" * col_w] * n_cols) + "┘"

        lines.append(top)
        lines.append(row_str(headers))
        lines.append(sep)

        for row in rows:
            if isinstance(row, dict):
                cells = [row.get(h, "") for h in headers]
            elif isinstance(row, (list, tuple)):
                cells = list(row)
            else:
                cells = [str(row)]
            # Pad or truncate to n_cols
            while len(cells) < n_cols:
                cells.append("")
            lines.append(row_str(cells[:n_cols]))

        if totals:
            lines.append(sep)
            tot_cells = [totals.get(h, "") for h in headers]
            lines.append(row_str(tot_cells[:n_cols]))

        lines.append(bot)
        return "\n".join(lines)
