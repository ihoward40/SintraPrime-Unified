# Convenience: generate all SDKs at once
# ---------------------------------------------------------------------------

def generate_all_sdks(spec: dict | str | Path, output_dir: str | Path = ".") -> dict[str, Path]:
    """Generate Python SDK, TypeScript SDK, and curl examples from OpenAPI spec."""
    spec_data = load_spec(spec)
    output_dir = Path(output_dir)
    output_dir.mkdir(parents=True, exist_ok=True)

    results = {}

    # Python SDK
    py_gen = PythonSDKGenerator(spec_data)
    results["python"] = py_gen.save(output_dir / "sintraprime_sdk.py")

    # TypeScript SDK
    ts_gen = TypeScriptSDKGenerator(spec_data)
    results["typescript"] = ts_gen.save(output_dir / "sintraprime_sdk.ts")

    # Curl examples
    curl_gen = CurlExampleGenerator(spec_data)
    results["curl"] = curl_gen.save_markdown(output_dir / "curl_examples.md")

    print(f"\n✅ All SDKs generated in: {output_dir}")
    return results


