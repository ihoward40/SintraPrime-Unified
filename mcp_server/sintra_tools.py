_task_registry: Dict[str, Dict] = {}
_task_counter = 0


def _schedule_task(description: str, when: str) -> ToolResult:
    """Schedule an autonomous task for execution."""
    global _task_counter
    _task_counter += 1
    task_id = f"task_{_task_counter:04d}"

    _task_registry[task_id] = {
        "id": task_id,
        "description": description,
        "scheduled_for": when,
        "status": "SCHEDULED",
        "created_at": datetime.now().isoformat(),
    }

    result = {
        "task_id": task_id,
        "description": description,
        "scheduled_for": when,
        "status": "SCHEDULED",
        "message": f"Task '{description}' scheduled for {when}",
        "monitor_url": f"sintra://tasks/{task_id}",
    }
    return ToolResult.text(json.dumps(result, indent=2))


