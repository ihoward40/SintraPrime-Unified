class PythonSDKGenerator:
    """Generates a complete Python SDK from OpenAPI 3.1 spec."""

    def __init__(self, spec: dict):
        self.spec = spec
        self.schemas = spec.get("components", {}).get("schemas", {})
        self.paths = spec.get("paths", {})
        self.info = spec.get("info", {})
        self.servers = spec.get("servers", [{}])
        self.base_url = self.servers[0].get("url", "https://api.example.com") if self.servers else ""

    def generate(self) -> str:
        """Generate complete Python SDK as a string."""
        sections = [
            self._generate_header(),
            self._generate_base_client(),
            self._generate_models(),
            self._generate_api_clients(),
            self._generate_main_client(),
            self._generate_footer(),
        ]
        return "\n\n".join(sections)

    def _generate_header(self) -> str:
        title = self.info.get("title", "API")
        version = self.info.get("version", "1.0.0")
        description = self.info.get("description", "")
        return f'''"""
{title} Python SDK — v{version}
{description}

Auto-generated by SintraPrime SDK Generator.
Do not edit manually — regenerate from OpenAPI spec.
"""

from __future__ import annotations
import json
import time
import urllib.request
import urllib.parse
import urllib.error
from typing import Any, Optional, Union
from dataclasses import dataclass, field, asdict
'''

    def _generate_base_client(self) -> str:
        return f'''
# ---------------------------------------------------------------------------
# Base HTTP Client
# ---------------------------------------------------------------------------

class APIError(Exception):
    """Raised when the API returns an error response."""
    def __init__(self, status_code: int, message: str, details: str = ""):
        self.status_code = status_code
        self.message = message
        self.details = details
        super().__init__(f"APIError {{status_code}}: {{message}}")


class BaseClient:
    """Base HTTP client with auth, retry, and rate limiting."""

    DEFAULT_BASE_URL = "{self.base_url}"
    MAX_RETRIES = 3
    RETRY_BACKOFF = 1.5  # seconds

    def __init__(
        self,
        api_key: str = "",
        base_url: str = "",
        timeout: int = 30,
        max_retries: int = 3,
    ):
        self.api_key = api_key
        self.base_url = (base_url or self.DEFAULT_BASE_URL).rstrip("/")
        self.timeout = timeout
        self.max_retries = max_retries

    def _headers(self) -> dict[str, str]:
        headers = {{
            "Content-Type": "application/json",
            "Accept": "application/json",
            "X-SDK-Version": "2.0.0",
            "X-SDK-Language": "python",
        }}
        if self.api_key:
            headers["Authorization"] = f"Bearer {{self.api_key}}"
        return headers

    def _request(
        self,
        method: str,
        path: str,
        params: dict | None = None,
        body: dict | None = None,
    ) -> dict:
        url = self.base_url + path
        if params:
            url += "?" + urllib.parse.urlencode({{k: v for k, v in params.items() if v is not None}})

        data = json.dumps(body).encode() if body else None
        req = urllib.request.Request(url, data=data, headers=self._headers(), method=method)

        last_error = None
        for attempt in range(self.max_retries):
            try:
                with urllib.request.urlopen(req, timeout=self.timeout) as resp:
                    response_body = resp.read().decode()
                    return json.loads(response_body) if response_body else {{}}
            except urllib.error.HTTPError as e:
                error_body = json.loads(e.read().decode()) if e.fp else {{}}
                if e.code == 429:  # Rate limited — always retry
                    wait = self.RETRY_BACKOFF ** attempt
                    time.sleep(wait)
                    last_error = e
                    continue
                raise APIError(e.code, error_body.get("message", str(e)), error_body.get("details", ""))
            except Exception as e:
                if attempt < self.max_retries - 1:
                    time.sleep(self.RETRY_BACKOFF ** attempt)
                    last_error = e
                    continue
                raise

        if last_error:
            raise last_error

    def get(self, path: str, params: dict | None = None) -> dict:
        return self._request("GET", path, params=params)

    def post(self, path: str, body: dict | None = None, params: dict | None = None) -> dict:
        return self._request("POST", path, params=params, body=body)

    def put(self, path: str, body: dict | None = None) -> dict:
        return self._request("PUT", path, body=body)

    def patch(self, path: str, body: dict | None = None) -> dict:
        return self._request("PATCH", path, body=body)

    def delete(self, path: str) -> dict:
        return self._request("DELETE", path)
'''

    def _generate_models(self) -> str:
        lines = ["# ---------------------------------------------------------------------------",
                 "# Data Models (auto-generated from OpenAPI schemas)",
                 "# ---------------------------------------------------------------------------", ""]
        for name, schema in self.schemas.items():
            if schema.get("type") != "object":
                continue
            props = schema.get("properties", {})
            required = schema.get("required", [])
            lines.append("@dataclass")
            lines.append(f"class {name}:")
            desc = schema.get("description", f"{name} model.")
            lines.append(f'    """{desc}"""')
            if not props:
                lines.append("    pass")
            for prop_name, prop_schema in props.items():
                py_type = get_python_type(prop_schema, self.schemas)
                default = "None" if prop_name not in required else "..."
                if default == "None":
                    lines.append(f"    {prop_name}: Optional[{py_type}] = None")
                else:
                    lines.append(f"    {prop_name}: {py_type}")
            lines.append("")
        return "\n".join(lines)

    def _generate_api_clients(self) -> str:
        """Group endpoints by tag and generate API client classes."""
        tag_operations: dict[str, list[dict]] = {}
        for path, path_item in self.paths.items():
            for method, operation in path_item.items():
                if not isinstance(operation, dict):
                    continue
                tags = operation.get("tags", ["Default"])
                for tag in tags:
                    tag_clean = tag.replace("/", "_").replace(" ", "")
                    if tag_clean not in tag_operations:
                        tag_operations[tag_clean] = []
                    tag_operations[tag_clean].append({
                        "path": path,
                        "method": method.upper(),
                        "operation": operation,
                    })

        lines = ["# ---------------------------------------------------------------------------",
                 "# API Client Classes (one per tag)",
                 "# ---------------------------------------------------------------------------", ""]

        for tag, operations in tag_operations.items():
            class_name = f"{to_pascal_case(tag)}API"
            lines.append(f"class {class_name}(BaseClient):")
            lines.append(f'    """API client for {tag} operations."""')
            lines.append("")
            for op_info in operations:
                method_lines = self._generate_python_method(op_info)
                for line in method_lines:
                    lines.append(f"    {line}")
                lines.append("")
            lines.append("")

        return "\n".join(lines)

    def _generate_python_method(self, op_info: dict) -> list[str]:
        """Generate a single Python method for an API operation."""
        path = op_info["path"]
        http_method = op_info["method"]
        operation = op_info["operation"]

        op_id = operation.get("operationId", "")
        method_name = to_snake_case(op_id) if op_id else f"{http_method.lower()}_{path.replace('/', '_').strip('_')}"
        summary = operation.get("summary", "")
        params = operation.get("parameters", [])
        request_body = operation.get("requestBody", {})

        # Build function signature
        args = ["self"]
        path_params = [p for p in params if p.get("in") == "path"]
        query_params = [p for p in params if p.get("in") == "query"]

        for p in path_params:
            p_name = to_snake_case(p["name"])
            if p_name in PYTHON_RESERVED_KEYWORDS:
                p_name = p_name + "_"
            p_type = get_python_type(p.get("schema", {"type": "string"}), self.schemas)
            args.append(f"{p_name}: {p_type}")

        if request_body:
            args.append("body: dict | None = None")

        for p in query_params:
            p_name = to_snake_case(p["name"])
            if p_name in PYTHON_RESERVED_KEYWORDS:
                p_name = p_name + "_"
            p_type = get_python_type(p.get("schema", {"type": "string"}), self.schemas)
            default = p.get("schema", {}).get("default", "None")
            if not p.get("required"):
                args.append(f"{p_name}: Optional[{p_type}] = {repr(default) if default != 'None' else 'None'}")

        sig = f"def {method_name}({', '.join(args)}) -> dict:"
        lines = [sig, f'    """{summary}"""']

        # Build actual path with parameter substitution
        py_path = re.sub(r"\{(\w+)\}", lambda m: "{" + to_snake_case(m.group(1)) + "}", path)
        format_path = f'path = f"{py_path}"'
        lines.append(f"    {format_path}")

        if query_params:
            qp_lines = ["    params = {"]
            for p in query_params:
                p_name = to_snake_case(p["name"])
                if p_name in PYTHON_RESERVED_KEYWORDS:
                    p_name = p_name + "_"
                qp_lines.append(f'        "{p["name"]}": {p_name},')
            qp_lines.append("    }")
            lines.extend(qp_lines)

        if http_method == "GET":
            params_arg = "params=params" if query_params else ""
            lines.append(f"    return self.get(path{', ' + params_arg if params_arg else ''})")
        elif http_method == "POST":
            lines.append("    return self.post(path, body=body)")
        elif http_method == "PUT":
            lines.append("    return self.put(path, body=body)")
        elif http_method == "PATCH":
            lines.append("    return self.patch(path, body=body)")
        elif http_method == "DELETE":
            lines.append("    return self.delete(path)")

        return lines

    def _generate_main_client(self) -> str:
        """Generate the top-level SintraPrime client."""
        title = self.info.get("title", "API").replace(" ", "").replace("-", "")
        tag_names = set()
        for path_item in self.paths.values():
            for operation in path_item.values():
                if isinstance(operation, dict):
                    for tag in operation.get("tags", []):
                        tag_names.add(tag.replace("/", "_").replace(" ", ""))

        init_lines = []
        attrs = []
        for tag in sorted(tag_names):
            class_name = f"{to_pascal_case(tag)}API"
            attr_name = to_snake_case(tag)
            init_lines.append(f"        self.{attr_name} = {class_name}(api_key=api_key, base_url=base_url, timeout=timeout)")
            attrs.append(f"    {attr_name}: {class_name}")

        return f'''
# ---------------------------------------------------------------------------
# Main Client
# ---------------------------------------------------------------------------

class {title.replace(" ", "")}Client:
    """
    Main SintraPrime-Unified API client.

    Usage:
        client = SintraPrimeUnifiedAPIClient(api_key="your-key")
        cases = client.legal_intelligence.search_case_law(query="landlord tenant")
        trust = client.trust_law.create_living_trust(body={{...}})
    """

    def __init__(
        self,
        api_key: str = "",
        base_url: str = "{self.base_url}",
        timeout: int = 30,
    ):
        self.api_key = api_key
        self.base_url = base_url
        self.timeout = timeout
{chr(10).join(init_lines)}

    def __repr__(self) -> str:
        return f"SintraPrimeClient(base_url={{self.base_url!r}})"


# Convenience alias
SintraPrimeClient = {title.replace(" ", "")}Client
'''

    def _generate_footer(self) -> str:
        return '''
# ---------------------------------------------------------------------------
# Module exports
# ---------------------------------------------------------------------------

__all__ = [
    "SintraPrimeClient",
    "APIError",
    "BaseClient",
]
'''

    def save(self, output_path: str | Path) -> Path:
        """Save generated SDK to file."""
        code = self.generate()
        path = Path(output_path)
        path.parent.mkdir(parents=True, exist_ok=True)
        path.write_text(code)
        print(f"✅ Python SDK saved: {path} ({len(code.splitlines())} lines)")
        return path


