# ---------------------------------------------------------------------------
# Data Models
# ---------------------------------------------------------------------------

@dataclass
class DocumentStyle:
    """Visual and formatting style for a document."""
    theme: str                          # legal | financial | corporate | modern | government
    font_family: str
    primary_color: str                  # hex color
    accent_color: str                   # hex color
    logo_text: str
    footer_text: str
    watermark: Optional[str] = None
    page_width: int = 80               # characters for plain-text rendering
    indent_size: int = 4


@dataclass
class RenderedDocument:
    """A fully rendered document in multiple formats."""
    title: str
    content_markdown: str
    content_html: str
    content_text: str
    style: DocumentStyle
    metadata: Dict[str, Any] = field(default_factory=dict)
    page_count: int = 1


@dataclass
class DocumentPackage:
    """A collection of related documents with cover page and TOC."""
    cover_page: str
    table_of_contents: str
    sections: List[RenderedDocument] = field(default_factory=list)
    unified_style: Optional[DocumentStyle] = None

    @property
    def full_text(self) -> str:
        parts = [self.cover_page, self.table_of_contents]
        for i, doc in enumerate(self.sections, 1):
            parts.append(f"\n{'='*80}\n  TAB {i}: {doc.title.upper()}\n{'='*80}\n")
            parts.append(doc.content_text)
        return "\n\n".join(parts)


