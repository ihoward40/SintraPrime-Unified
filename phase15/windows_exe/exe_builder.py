"""
Phase 15B — Windows Desktop Executable Builder
Generates a 1-click PyInstaller spec, build script, NSIS installer script,
and Windows taskbar tray icon integration for SintraPrime.

Usage:
    builder = ExeBuilder(config)
    builder.generate_all()   # writes all files to output_dir
    builder.build()          # runs PyInstaller (Windows only)
"""
from __future__ import annotations

import json
import os
import platform
import shutil
import subprocess
import sys
import textwrap
from dataclasses import dataclass, field
from pathlib import Path
from typing import Any, Dict, List, Optional


# ---------------------------------------------------------------------------
# Configuration
# ---------------------------------------------------------------------------

@dataclass
class ExeConfig:
    app_name: str = "SintraPrime"
    app_version: str = "1.0.0"
    app_description: str = "AI-Powered Legal Assistant"
    company_name: str = "SintraPrime Inc."
    author: str = "SintraPrime Team"
    main_script: str = "main.py"
    icon_path: str = "assets/sintra_icon.ico"
    output_dir: str = "dist"
    build_dir: str = "build"
    one_file: bool = True
    windowed: bool = True  # No console window
    uac_admin: bool = False
    add_data: List[str] = field(default_factory=list)
    hidden_imports: List[str] = field(default_factory=list)
    exclude_modules: List[str] = field(default_factory=list)
    runtime_hooks: List[str] = field(default_factory=list)
    nsis_output_dir: str = "installer"
    tray_enabled: bool = True
    auto_start: bool = False  # Add to Windows startup
    port: int = 8765  # Local API server port

    def to_dict(self) -> Dict[str, Any]:
        return {
            "app_name": self.app_name,
            "app_version": self.app_version,
            "app_description": self.app_description,
            "company_name": self.company_name,
            "author": self.author,
            "main_script": self.main_script,
            "icon_path": self.icon_path,
            "output_dir": self.output_dir,
            "one_file": self.one_file,
            "windowed": self.windowed,
            "tray_enabled": self.tray_enabled,
            "auto_start": self.auto_start,
            "port": self.port,
        }


# ---------------------------------------------------------------------------
# PyInstaller spec generator
# ---------------------------------------------------------------------------

class SpecGenerator:
    """Generates a .spec file for PyInstaller."""

    def __init__(self, config: ExeConfig):
        self.config = config

    def generate(self) -> str:
        c = self.config
        add_data_str = ", ".join(
            f"('{src}', '{dst}')" for src, dst in
            [d.split(";") for d in c.add_data] if ";" in d
        ) if c.add_data else ""

        hidden_imports_str = ", ".join(f"'{m}'" for m in c.hidden_imports)
        exclude_str = ", ".join(f"'{m}'" for m in c.exclude_modules)
        runtime_hooks_str = ", ".join(f"'{h}'" for h in c.runtime_hooks)

        mode = "EXE" if c.one_file else "COLLECT"
        console_flag = "False" if c.windowed else "True"
        uac_flag = "True" if c.uac_admin else "False"

        spec = textwrap.dedent(f"""\
            # -*- mode: python ; coding: utf-8 -*-
            # Auto-generated by SintraPrime ExeBuilder v{c.app_version}

            block_cipher = None

            a = Analysis(
                ['{c.main_script}'],
                pathex=[],
                binaries=[],
                datas=[{add_data_str}],
                hiddenimports=[{hidden_imports_str}],
                hookspath=[],
                hooksconfig={{}},
                runtime_hooks=[{runtime_hooks_str}],
                excludes=[{exclude_str}],
                win_no_prefer_redirects=False,
                win_private_assemblies=False,
                cipher=block_cipher,
                noarchive=False,
            )

            pyz = PYZ(a.pure, a.zipped_data, cipher=block_cipher)

            exe = EXE(
                pyz,
                a.scripts,
                {'a.binaries,' if c.one_file else '[],'} 
                {'a.zipfiles,' if c.one_file else '[],'} 
                {'a.datas,' if c.one_file else '[],'} 
                [],
                name='{c.app_name}',
                debug=False,
                bootloader_ignore_signals=False,
                strip=False,
                upx=True,
                upx_exclude=[],
                runtime_tmpdir=None,
                console={console_flag},
                disable_windowed_traceback=False,
                argv_emulation=False,
                target_arch=None,
                codesign_identity=None,
                entitlements_file=None,
                icon='{c.icon_path}',
                uac_admin={uac_flag},
                version='version_info.txt',
            )
        """)

        if not c.one_file:
            spec += textwrap.dedent(f"""
            coll = COLLECT(
                exe,
                a.binaries,
                a.zipfiles,
                a.datas,
                strip=False,
                upx=True,
                upx_exclude=[],
                name='{c.app_name}',
            )
            """)

        return spec

    def generate_version_info(self) -> str:
        c = self.config
        major, minor, patch = (c.app_version.split(".") + ["0", "0", "0"])[:3]
        return textwrap.dedent(f"""\
            VSVersionInfo(
              ffi=FixedFileInfo(
                filevers=({major}, {minor}, {patch}, 0),
                prodvers=({major}, {minor}, {patch}, 0),
                mask=0x3f,
                flags=0x0,
                OS=0x40004,
                fileType=0x1,
                subtype=0x0,
                date=(0, 0)
              ),
              kids=[
                StringFileInfo([
                  StringTable(
                    u'040904B0',
                    [StringStruct(u'CompanyName', u'{c.company_name}'),
                     StringStruct(u'FileDescription', u'{c.app_description}'),
                     StringStruct(u'FileVersion', u'{c.app_version}'),
                     StringStruct(u'InternalName', u'{c.app_name}'),
                     StringStruct(u'LegalCopyright', u'Copyright 2025 {c.company_name}'),
                     StringStruct(u'OriginalFilename', u'{c.app_name}.exe'),
                     StringStruct(u'ProductName', u'{c.app_name}'),
                     StringStruct(u'ProductVersion', u'{c.app_version}')])
                ]),
                VarFileInfo([VarStruct(u'Translation', [1033, 1200])])
              ]
            )
        """)


