class DocumentRenderer:
    """
    Renders professional documents in multiple formats.
    All output meets the quality standards of a top law firm or Big 4 firm.
    """

    DEFAULT_STYLE = SINTRAPRIME_SIGNATURE

    def __init__(self, default_style: Optional[DocumentStyle] = None):
        self.default_style = default_style or self.DEFAULT_STYLE

    # ------------------------------------------------------------------
    # Public API
    # ------------------------------------------------------------------

    def render_legal_document(
        self,
        doc: Dict[str, Any],
        style: Optional[DocumentStyle] = None,
    ) -> RenderedDocument:
        """Render a professional legal document."""
        style = style or self.default_style
        w = style.page_width

        title = doc.get("title", "LEGAL DOCUMENT")
        court = doc.get("court", "")
        case_number = doc.get("case_number", "")
        plaintiff = doc.get("plaintiff", "[PLAINTIFF]")
        defendant = doc.get("defendant", "[DEFENDANT]")
        attorney = doc.get("attorney", "[ATTORNEY NAME]")
        bar_number = doc.get("bar_number", "[BAR NO.]")
        firm = doc.get("firm", "[LAW FIRM NAME]")
        sections = doc.get("sections", [])
        date_str = doc.get("date", _fmt.format_date())

        lines: List[str] = []

        # Header / Logo
        lines.append(_fmt.horizontal_rule(w, "═"))
        lines.append(_fmt.center(style.logo_text, w))
        lines.append(_fmt.horizontal_rule(w, "═"))
        lines.append("")

        # Court caption
        if court:
            lines.append(_fmt.center(court.upper(), w))
            lines.append(_fmt.horizontal_rule(w, "─"))
        if case_number:
            lines.append(_fmt.center(f"Case No.: {case_number}", w))
        lines.append("")

        # Parties
        lines.append(f"  {plaintiff.upper()},")
        lines.append("                                               Plaintiff,")
        lines.append("")
        lines.append("    v.")
        lines.append("")
        lines.append(f"  {defendant.upper()},")
        lines.append("                                               Defendant.")
        lines.append(_fmt.horizontal_rule(w, "─"))
        lines.append("")

        # Document title
        lines.append(_fmt.center(title.upper(), w))
        lines.append(_fmt.center(f"Dated: {date_str}", w))
        lines.append("")
        lines.append(_fmt.horizontal_rule(w, "═"))
        lines.append("")

        # Introduction / Comes Now
        intro = doc.get("introduction", f"COMES NOW {plaintiff}, by and through undersigned counsel, and respectfully submits this {title} as follows:")
        lines.append(_fmt.wrap(intro, w - 4, "    "))
        lines.append("")

        # Numbered sections with Roman numerals
        if sections:
            for idx, sec in enumerate(sections, 1):
                sec_title = sec.get("title", f"SECTION {idx}")
                lines.append(_fmt.roman_section(idx, sec_title))
                lines.append("")
                # Sub-sections (A., B., C.)
                subsections = sec.get("subsections", [])
                if subsections:
                    for sidx, sub in enumerate(subsections):
                        sub_label = chr(ord("A") + sidx)
                        sub_title = sub.get("title", "")
                        lines.append(f"  {sub_label}. {sub_title}")
                        sub_body = sub.get("body", "")
                        if sub_body:
                            lines.append(_fmt.wrap(sub_body, w - 8, "      "))
                        # numbered items
                        items = sub.get("items", [])
                        if items:
                            lines.append(_fmt.numbered_list(items))
                        # lettered sub-items
                        alpha_items = sub.get("alpha_items", [])
                        if alpha_items:
                            lines.append(_fmt.alpha_list(alpha_items))
                        lines.append("")
                else:
                    body = sec.get("body", "")
                    if body:
                        lines.append(_fmt.wrap(body, w - 4, "    "))
                    items = sec.get("items", [])
                    if items:
                        lines.append(_fmt.numbered_list(items))
                    lines.append("")
        else:
            lines.append("    [DOCUMENT BODY — INSERT CONTENT HERE]")
            lines.append("")

        # Prayer for Relief / Conclusion
        conclusion = doc.get("conclusion", "WHEREFORE, the foregoing premises considered, undersigned counsel respectfully requests that this Court grant the relief requested herein.")
        lines.append(_fmt.horizontal_rule(w, "─"))
        lines.append("")
        lines.append("    " + _fmt.wrap(conclusion, w - 4, "    ").strip())
        lines.append("")

        # Signature block
        lines += self._signature_block_legal(
            attorney=attorney,
            bar_number=bar_number,
            firm=firm,
            date_str=date_str,
            doc=doc,
            width=w,
        )

        # Certificate of service
        lines += self._certificate_of_service(doc, w)

        # Footer
        lines.append(_fmt.horizontal_rule(w, "═"))
        lines.append(_fmt.center(style.footer_text, w))
        lines.append(_fmt.horizontal_rule(w, "═"))

        content_text = "\n".join(lines)
        content_html = _fmt.text_to_html(content_text, title, style)
        content_markdown = _fmt.text_to_markdown(content_text, title)

        page_count = max(1, len(lines) // 55)
        return RenderedDocument(
            title=title,
            content_markdown=content_markdown,
            content_html=content_html,
            content_text=content_text,
            style=style,
            metadata={"court": court, "case_number": case_number, "date": date_str},
            page_count=page_count,
        )

    def render_financial_report(
        self,
        report: Dict[str, Any],
        style: Optional[DocumentStyle] = None,
    ) -> RenderedDocument:
        """Render a professional financial report."""
        style = style or self.default_style
        w = style.page_width

        title = report.get("title", "FINANCIAL REPORT")
        subtitle = report.get("subtitle", "")
        prepared_by = report.get("prepared_by", "[PREPARER NAME]")
        prepared_for = report.get("prepared_for", "[RECIPIENT]")
        date_str = report.get("date", _fmt.format_date())
        sections = report.get("sections", [])
        key_metrics = report.get("key_metrics", {})
        footnotes = report.get("footnotes", [])

        lines: List[str] = []

        # Header box
        header_lines = [
            _fmt.center(style.logo_text, w - 4),
            "",
            _fmt.center(title.upper(), w - 4),
        ]
        if subtitle:
            header_lines.append(_fmt.center(subtitle, w - 4))
        header_lines += [
            "",
            _fmt.center(f"As of: {date_str}", w - 4),
        ]
        lines.append(_fmt.box(header_lines, w))
        lines.append("")

        # Prepared by / for
        lines.append(f"  Prepared By:  {prepared_by}")
        lines.append(f"  Prepared For: {prepared_for}")
        lines.append(f"  Date:         {date_str}")
        lines.append(_fmt.horizontal_rule(w, "─"))
        lines.append("")

        # Key metrics executive summary
        if key_metrics:
            lines.append("  EXECUTIVE SUMMARY — KEY METRICS")
            lines.append("  " + "─" * (w - 4))
            col_w = (w - 6) // 2
            for k, v in key_metrics.items():
                label = str(k)[:col_w - 2].ljust(col_w - 2)
                val = str(v)
                lines.append(f"  │ {label}  {val}")
            lines.append("  " + "─" * (w - 4))
            lines.append("")

        # Report sections
        for idx, sec in enumerate(sections, 1):
            sec_title = sec.get("title", f"Section {idx}")
            lines.append(_fmt.section_header(str(idx), sec_title, w))
            body = sec.get("body", "")
            if body:
                lines.append(_fmt.wrap(body, w - 4, "  "))
                lines.append("")
            # Tables
            tables = sec.get("tables", [])
            for tbl in tables:
                lines.append(self._render_financial_table(tbl, w))
                lines.append("")

        # Footnotes
        if footnotes:
            lines.append(_fmt.horizontal_rule(w, "─"))
            lines.append("  FOOTNOTES AND DISCLAIMERS")
            for i, fn in enumerate(footnotes, 1):
                lines.append(f"  ({i}) {_fmt.wrap(fn, w - 6, '      ').strip()}")
            lines.append("")

        # Disclaimer
        lines.append(_fmt.horizontal_rule(w, "─"))
        disclaimer = report.get("disclaimer", "This report is prepared for informational purposes only. Past performance does not guarantee future results. This is not investment advice.")
        lines.append("  DISCLAIMER:")
        lines.append(_fmt.wrap(disclaimer, w - 4, "  "))
        lines.append("")

        # Footer
        lines.append(_fmt.horizontal_rule(w, "═"))
        lines.append(_fmt.center(style.footer_text, w))
        lines.append(_fmt.horizontal_rule(w, "═"))

        content_text = "\n".join(lines)
        return RenderedDocument(
            title=title,
            content_markdown=_fmt.text_to_markdown(content_text, title),
            content_html=_fmt.text_to_html(content_text, title, style),
            content_text=content_text,
            style=style,
            metadata={"prepared_by": prepared_by, "date": date_str},
            page_count=max(1, len(lines) // 55),
        )

    def render_letter(
        self,
        letter: Dict[str, Any],
        style: Optional[DocumentStyle] = None,
    ) -> RenderedDocument:
        """Render a professional business letter in full block format."""
        style = style or self.default_style
        w = style.page_width

        sender_name = letter.get("sender_name", "[SENDER NAME]")
        sender_title = letter.get("sender_title", "")
        sender_firm = letter.get("sender_firm", "")
        sender_address = letter.get("sender_address", "[ADDRESS]")
        sender_city_state = letter.get("sender_city_state", "[CITY, STATE ZIP]")
        sender_phone = letter.get("sender_phone", "")
        sender_email = letter.get("sender_email", "")

        recipient_name = letter.get("recipient_name", "[RECIPIENT NAME]")
        recipient_title = letter.get("recipient_title", "")
        recipient_firm = letter.get("recipient_firm", "")
        recipient_address = letter.get("recipient_address", "[ADDRESS]")
        recipient_city_state = letter.get("recipient_city_state", "[CITY, STATE ZIP]")

        date_str = letter.get("date", _fmt.format_date())
        subject = letter.get("subject", "")
        salutation = letter.get("salutation", f"Dear {recipient_name}:")
        body_paragraphs = letter.get("body_paragraphs", ["[LETTER BODY]"])
        closing = letter.get("closing", "Very truly yours,")
        cc_list = letter.get("cc", [])
        enclosures = letter.get("enclosures", [])
        via = letter.get("via", "")

        lines: List[str] = []

        # Letterhead
        lines.append(_fmt.horizontal_rule(w, "═"))
        lines.append(_fmt.center(style.logo_text, w))
        if sender_firm:
            lines.append(_fmt.center(sender_firm.upper(), w))
        lines.append(_fmt.center(sender_address, w))
        lines.append(_fmt.center(sender_city_state, w))
        if sender_phone:
            lines.append(_fmt.center(f"Tel: {sender_phone}", w))
        if sender_email:
            lines.append(_fmt.center(f"Email: {sender_email}", w))
        lines.append(_fmt.horizontal_rule(w, "═"))
        lines.append("")

        # Date
        lines.append(date_str)
        lines.append("")

        # Via line
        if via:
            lines.append(via)
            lines.append("")

        # Recipient address
        lines.append(recipient_name)
        if recipient_title:
            lines.append(recipient_title)
        if recipient_firm:
            lines.append(recipient_firm)
        lines.append(recipient_address)
        lines.append(recipient_city_state)
        lines.append("")

        # Re: line
        if subject:
            lines.append(f"Re:    {subject}")
            lines.append(_fmt.horizontal_rule(min(60, w), "─"))
            lines.append("")

        # Salutation
        lines.append(salutation)
        lines.append("")

        # Body paragraphs
        for para in body_paragraphs:
            lines.append(_fmt.wrap(para, w - 2, "  "))
            lines.append("")

        # Closing
        lines.append(closing)
        lines.append("")
        lines.append("")
        lines.append("")
        lines.append(f"  {sender_name}")
        if sender_title:
            lines.append(f"  {sender_title}")
        if sender_firm:
            lines.append(f"  {sender_firm}")
        lines.append("")

        # CC
        if cc_list:
            lines.append("cc:    " + cc_list[0])
            for cc in cc_list[1:]:
                lines.append("       " + cc)
            lines.append("")

        # Enclosures
        if enclosures:
            lines.append(f"Encl:  ({len(enclosures)})")
            for enc in enclosures:
                lines.append(f"       {enc}")
            lines.append("")

        # Footer
        lines.append(_fmt.horizontal_rule(w, "─"))
        lines.append(_fmt.center(style.footer_text, w))

        title = f"Letter — {subject or recipient_name}"
        content_text = "\n".join(lines)
        return RenderedDocument(
            title=title,
            content_markdown=_fmt.text_to_markdown(content_text, title),
            content_html=_fmt.text_to_html(content_text, title, style),
            content_text=content_text,
            style=style,
            metadata={"date": date_str, "recipient": recipient_name},
            page_count=max(1, len(lines) // 55),
        )

    def render_contract(
        self,
        contract: Dict[str, Any],
        style: Optional[DocumentStyle] = None,
    ) -> RenderedDocument:
        """Render a professional contract with full formatting."""
        style = style or self.default_style
        w = style.page_width

        title = contract.get("title", "AGREEMENT")
        effective_date = contract.get("effective_date", "[DATE]")
        parties = contract.get("parties", [])
        recitals = contract.get("recitals", [])
        sections = contract.get("sections", [])
        exhibits = contract.get("exhibits", [])
        governing_law = contract.get("governing_law", "[STATE]")
        date_str = contract.get("date", _fmt.format_date())

        lines: List[str] = []

        # Header
        lines.append(_fmt.horizontal_rule(w, "═"))
        lines.append(_fmt.center(style.logo_text, w))
        lines.append(_fmt.horizontal_rule(w, "═"))
        lines.append("")
        lines.append(_fmt.center(title.upper(), w))
        lines.append("")
        lines.append(_fmt.center(f"Effective Date: {effective_date}", w))
        lines.append(_fmt.horizontal_rule(w, "─"))
        lines.append("")

        # Parties
        if parties:
            lines.append("PARTIES:")
            lines.append("")
            for p in parties:
                p_name = p.get("name", "[PARTY NAME]")
                p_type = p.get("type", "Party")
                p_addr = p.get("address", "")
                p_state = p.get("state", "")
                lines.append(f"  {p_type.upper()}: {p_name.upper()} (\"{''.join(w for w in p_type.split()).upper()}\")")
                if p_addr:
                    lines.append(f"    Address: {p_addr}")
                if p_state:
                    lines.append(f"    State of Formation: {p_state}")
                lines.append("")

        # Recitals (WHEREAS clauses)
        if recitals:
            lines.append(_fmt.horizontal_rule(w, "─"))
            lines.append("RECITALS")
            lines.append("")
            for i, rec in enumerate(recitals):
                lines.append(f"  WHEREAS, {_fmt.wrap(rec, w - 12, '           ').strip()};")
                lines.append("")
            lines.append("  NOW, THEREFORE, in consideration of the mutual promises, covenants, and")
            lines.append("  agreements set forth herein, and for other good and valuable consideration,")
            lines.append("  the receipt and sufficiency of which are hereby acknowledged, the parties")
            lines.append("  agree as follows:")
            lines.append("")

        # Sections
        if sections:
            for idx, sec in enumerate(sections, 1):
                sec_title = sec.get("title", f"SECTION {idx}")
                lines.append(f"SECTION {idx}. {sec_title.upper()}")
                lines.append("")
                subsections = sec.get("subsections", [])
                if subsections:
                    for sidx, sub in enumerate(subsections, 1):
                        sub_title = sub.get("title", "")
                        sub_body = sub.get("body", "")
                        lines.append(f"  {idx}.{sidx} {sub_title}")
                        if sub_body:
                            lines.append(_fmt.wrap(sub_body, w - 6, "      "))
                        lines.append("")
                else:
                    body = sec.get("body", "")
                    if body:
                        lines.append(_fmt.wrap(body, w - 4, "    "))
                    lines.append("")
        else:
            lines.append("SECTION 1. TERMS AND CONDITIONS")
            lines.append("")
            lines.append("    [INSERT AGREEMENT TERMS HERE]")
            lines.append("")

        # Governing Law
        lines.append(f"GOVERNING LAW. This Agreement shall be governed by the laws of the State")
        lines.append(f"of {governing_law}, without regard to its conflict of law provisions.")
        lines.append("")

        # Exhibits
        if exhibits:
            lines.append(_fmt.horizontal_rule(w, "─"))
            lines.append("EXHIBITS:")
            for ex in exhibits:
                lines.append(f"  Exhibit {ex.get('label', 'A')} — {ex.get('title', '[EXHIBIT TITLE]')}")
            lines.append("")

        # Signature page
        lines += self._signature_page_contract(parties, date_str, w)

        # Footer
        lines.append(_fmt.horizontal_rule(w, "═"))
        lines.append(_fmt.center(style.footer_text, w))
        lines.append(_fmt.horizontal_rule(w, "═"))

        content_text = "\n".join(lines)
        return RenderedDocument(
            title=title,
            content_markdown=_fmt.text_to_markdown(content_text, title),
            content_html=_fmt.text_to_html(content_text, title, style),
            content_text=content_text,
            style=style,
            metadata={"effective_date": effective_date, "governing_law": governing_law},
            page_count=max(1, len(lines) // 55),
        )

    def create_document_package(
        self,
        documents: List[Dict[str, Any]],
        cover_page: Dict[str, Any],
        style: Optional[DocumentStyle] = None,
    ) -> DocumentPackage:
        """Create a multi-document package with cover page and TOC."""
        style = style or self.default_style
        w = style.page_width

        pkg_title = cover_page.get("title", "DOCUMENT PACKAGE")
        pkg_subtitle = cover_page.get("subtitle", "")
        pkg_date = cover_page.get("date", _fmt.format_date())
        pkg_prepared_by = cover_page.get("prepared_by", style.logo_text)
        confidentiality = cover_page.get(
            "confidentiality",
            "CONFIDENTIAL — This document package contains privileged and confidential information. "
            "Unauthorized use, disclosure, or distribution is strictly prohibited.",
        )

        # Build cover page
        cover_lines = [
            _fmt.horizontal_rule(w, "═"),
            _fmt.center(style.logo_text, w),
            _fmt.horizontal_rule(w, "═"),
            "",
            "",
            _fmt.center("━" * 40, w),
            _fmt.center(pkg_title.upper(), w),
            "",
        ]
        if pkg_subtitle:
            cover_lines.append(_fmt.center(pkg_subtitle, w))
        cover_lines += [
            _fmt.center("━" * 40, w),
            "",
            "",
            _fmt.center(f"Prepared: {pkg_date}", w),
            _fmt.center(f"By: {pkg_prepared_by}", w),
            "",
            "",
            _fmt.horizontal_rule(w, "─"),
            _fmt.wrap(confidentiality, w - 4, "  "),
            _fmt.horizontal_rule(w, "─"),
        ]
        cover_text = "\n".join(cover_lines)

        # Render each document
        rendered: List[RenderedDocument] = []
        doc_renderer_map = {
            "legal": self.render_legal_document,
            "financial": self.render_financial_report,
            "letter": self.render_letter,
            "contract": self.render_contract,
        }

        for doc in documents:
            doc_type = doc.get("type", "legal")
            renderer = doc_renderer_map.get(doc_type, self.render_legal_document)
            rendered.append(renderer(doc, style))

        # Build TOC
        toc_lines = [
            _fmt.horizontal_rule(w, "═"),
            _fmt.center("TABLE OF CONTENTS", w),
            _fmt.horizontal_rule(w, "═"),
            "",
        ]
        page = 2
        for i, doc in enumerate(rendered, 1):
            tab_label = f"Tab {i}: {doc.title}"
            page_ref = f"Page {page}"
            toc_lines.append(f"  {tab_label:<{w - 20}}  {page_ref:>10}")
            page += doc.page_count
        toc_lines.append("")
        toc_text = "\n".join(toc_lines)

        return DocumentPackage(
            cover_page=cover_text,
            table_of_contents=toc_text,
            sections=rendered,
            unified_style=style,
        )

