class PowerShellSetupGenerator:
    """Generates the PowerShell setup/install script."""

    def generate(self, cfg: AppConfig) -> DeploymentArtifact:
        startup_block = ""
        if cfg.enable_startup:
            startup_block = f"""
# Add to Windows startup
$startupKey = "HKCU:\\Software\\Microsoft\\Windows\\CurrentVersion\\Run"
Set-ItemProperty -Path $startupKey -Name "{cfg.name}" -Value '"{cfg.install_dir}\\{cfg.name}.exe"'
Write-Host "  Added to startup" -ForegroundColor Green
"""
        content = f"""#Requires -RunAsAdministrator
<#
.SYNOPSIS
    {cfg.name} v{cfg.version} — Windows Setup Script
.DESCRIPTION
    {cfg.description}
    Generated by SintraPrime Windows Deployer
.AUTHOR
    {cfg.author}
#>

param(
    [string]$InstallDir = "{cfg.install_dir}",
    [string]$DataDir = "$env:APPDATA\\{cfg.name}",
    [switch]$Silent,
    [switch]$Uninstall
)

$ErrorActionPreference = "Stop"
$ProgressPreference = "SilentlyContinue"

function Write-Step {{
    param([string]$Message)
    if (-not $Silent) {{ Write-Host "  >> $Message" -ForegroundColor Cyan }}
}}

function Test-PythonVersion {{
    try {{
        $ver = (python --version 2>&1).ToString()
        return $ver -match "Python {cfg.python_version}"
    }} catch {{ return $false }}
}}

# ── Uninstall mode ────────────────────────────────────────────────────────────
if ($Uninstall) {{
    Write-Host "Uninstalling {cfg.name}..." -ForegroundColor Yellow
    if (Test-Path $InstallDir) {{
        Remove-Item -Recurse -Force $InstallDir
        Write-Step "Removed installation directory"
    }}
    if (Test-Path $DataDir) {{
        $confirm = Read-Host "Remove user data? (y/N)"
        if ($confirm -eq "y") {{ Remove-Item -Recurse -Force $DataDir }}
    }}
    # Remove registry entries
    Remove-ItemProperty -Path "HKLM:\\Software\\Microsoft\\Windows\\CurrentVersion\\Uninstall\\{cfg.name}" -ErrorAction SilentlyContinue
    Write-Host "{cfg.name} uninstalled successfully." -ForegroundColor Green
    exit 0
}}

# ── Install mode ──────────────────────────────────────────────────────────────
Write-Host ""
Write-Host "  ╔══════════════════════════════════════╗" -ForegroundColor Magenta
Write-Host "  ║   {cfg.name} v{cfg.version} Installer         ║" -ForegroundColor Magenta
Write-Host "  ╚══════════════════════════════════════╝" -ForegroundColor Magenta
Write-Host ""

# Check Python
Write-Step "Checking Python {cfg.python_version}..."
if (-not (Test-PythonVersion)) {{
    Write-Host "Python {cfg.python_version} not found. Downloading..." -ForegroundColor Yellow
    $pyUrl = "https://www.python.org/ftp/python/{cfg.python_version}.0/python-{cfg.python_version}.0-amd64.exe"
    $pyInstaller = "$env:TEMP\\python_setup.exe"
    Invoke-WebRequest -Uri $pyUrl -OutFile $pyInstaller
    Start-Process -Wait -FilePath $pyInstaller -ArgumentList "/quiet", "InstallAllUsers=1", "PrependPath=1"
    Remove-Item $pyInstaller
}}

# Create directories
Write-Step "Creating directories..."
New-Item -ItemType Directory -Force -Path $InstallDir | Out-Null
New-Item -ItemType Directory -Force -Path $DataDir | Out-Null
New-Item -ItemType Directory -Force -Path "$DataDir\\logs" | Out-Null
New-Item -ItemType Directory -Force -Path "$DataDir\\db" | Out-Null

# Install Python dependencies
Write-Step "Installing Python dependencies..."
python -m pip install --quiet --upgrade pip
python -m pip install --quiet -r "$InstallDir\\requirements.txt"

# Create .env file if not exists
$envFile = "$DataDir\\.env"
if (-not (Test-Path $envFile)) {{
    Write-Step "Creating default configuration..."
    @"
SINTRA_DATA_DIR=$DataDir
SINTRA_LOG_LEVEL=INFO
SINTRA_AUTO_UPDATE={("true" if cfg.enable_auto_update else "false")}
OPENAI_API_KEY=
AIRTABLE_API_KEY=
STRIPE_SECRET_KEY=
"@ | Out-File -FilePath $envFile -Encoding UTF8
}}

# Registry entries for Add/Remove Programs
Write-Step "Registering application..."
$regPath = "HKLM:\\Software\\Microsoft\\Windows\\CurrentVersion\\Uninstall\\{cfg.name}"
New-Item -Path $regPath -Force | Out-Null
Set-ItemProperty -Path $regPath -Name "DisplayName" -Value "{cfg.name}"
Set-ItemProperty -Path $regPath -Name "DisplayVersion" -Value "{cfg.version}"
Set-ItemProperty -Path $regPath -Name "Publisher" -Value "{cfg.author}"
Set-ItemProperty -Path $regPath -Name "InstallLocation" -Value $InstallDir
Set-ItemProperty -Path $regPath -Name "UninstallString" -Value "powershell -File `"$InstallDir\\setup.ps1`" -Uninstall"
Set-ItemProperty -Path $regPath -Name "NoModify" -Value 1
Set-ItemProperty -Path $regPath -Name "NoRepair" -Value 1
{startup_block}
# Desktop shortcut
Write-Step "Creating desktop shortcut..."
$WshShell = New-Object -ComObject WScript.Shell
$Shortcut = $WshShell.CreateShortcut("$env:PUBLIC\\Desktop\\{cfg.name}.lnk")
$Shortcut.TargetPath = "$InstallDir\\{cfg.name}.exe"
$Shortcut.WorkingDirectory = $InstallDir
$Shortcut.Description = "{cfg.description}"
$Shortcut.Save()

Write-Host ""
Write-Host "  {cfg.name} v{cfg.version} installed successfully!" -ForegroundColor Green
Write-Host "  Launch from Desktop shortcut or: $InstallDir\\{cfg.name}.exe" -ForegroundColor Cyan
Write-Host ""
"""
        return DeploymentArtifact(
            artifact_id=f"ps_{uuid.uuid4().hex[:6]}",
            artifact_type="powershell_setup",
            filename="setup.ps1",
            content=content,
            description="PowerShell installation script (requires admin)",
        )


