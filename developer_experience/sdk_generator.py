def load_spec(spec_source: dict | str | Path) -> dict:
    """Load OpenAPI spec from dict, JSON string, or file path."""
    if isinstance(spec_source, dict):
        return spec_source
    if isinstance(spec_source, (str, Path)):
        path = Path(spec_source)
        if path.exists():
            with open(path) as f:
                return json.load(f)
        # Try parsing as JSON string
        return json.loads(spec_source)
    raise ValueError(f"Cannot load spec from: {type(spec_source)}")


