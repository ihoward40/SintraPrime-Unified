class TypeScriptSDKGenerator:
    """Generates a TypeScript/JavaScript SDK from OpenAPI 3.1 spec."""

    def __init__(self, spec: dict):
        self.spec = spec
        self.schemas = spec.get("components", {}).get("schemas", {})
        self.paths = spec.get("paths", {})
        self.info = spec.get("info", {})
        self.servers = spec.get("servers", [{}])
        self.base_url = self.servers[0].get("url", "") if self.servers else ""

    def generate(self) -> str:
        sections = [
            self._generate_header(),
            self._generate_types(),
            self._generate_client(),
            self._generate_api_classes(),
            self._generate_exports(),
        ]
        return "\n\n".join(sections)

    def _generate_header(self) -> str:
        title = self.info.get("title", "API")
        version = self.info.get("version", "1.0.0")
        return f'''/**
 * {title} TypeScript SDK — v{version}
 * Auto-generated by SintraPrime SDK Generator.
 * Do not edit manually.
 */

export class APIError extends Error {{
  constructor(
    public statusCode: number,
    public message: string,
    public details?: string
  ) {{
    super(`APIError ${{statusCode}}: ${{message}}`);
    this.name = "APIError";
  }}
}}

export interface RequestOptions {{
  timeout?: number;
  headers?: Record<string, string>;
}}

export interface ClientConfig {{
  apiKey?: string;
  baseUrl?: string;
  timeout?: number;
  maxRetries?: number;
}}'''

    def _generate_types(self) -> str:
        lines = ["// Auto-generated TypeScript interfaces from OpenAPI schemas", ""]
        for name, schema in self.schemas.items():
            if schema.get("type") != "object":
                continue
            props = schema.get("properties", {})
            required = set(schema.get("required", []))
            desc = schema.get("description", "")
            if desc:
                lines.append(f"/** {desc} */")
            lines.append(f"export interface {name} {{")
            for prop_name, prop_schema in props.items():
                ts_type = get_ts_type(prop_schema, self.schemas)
                optional = "" if prop_name in required else "?"
                example = prop_schema.get("example", "")
                comment = f"  // e.g. {example}" if example else ""
                lines.append(f"  {prop_name}{optional}: {ts_type};{comment}")
            lines.append("}")
            lines.append("")
        return "\n".join(lines)

    def _generate_client(self) -> str:
        return f'''// Base HTTP client
export class BaseClient {{
  protected readonly apiKey: string;
  protected readonly baseUrl: string;
  protected readonly timeout: number;
  protected readonly maxRetries: number;

  constructor(config: ClientConfig = {{}}) {{
    this.apiKey = config.apiKey ?? "";
    this.baseUrl = (config.baseUrl ?? "{self.base_url}").replace(/\\/$/, "");
    this.timeout = config.timeout ?? 30000;
    this.maxRetries = config.maxRetries ?? 3;
  }}

  protected async request<T = any>(
    method: string,
    path: string,
    options: {{ params?: Record<string, any>; body?: any }} = {{}}
  ): Promise<T> {{
    let url = this.baseUrl + path;
    if (options.params) {{
      const filteredParams = Object.fromEntries(
        Object.entries(options.params).filter(([_, v]) => v !== undefined && v !== null)
      );
      url += "?" + new URLSearchParams(filteredParams).toString();
    }}

    const headers: Record<string, string> = {{
      "Content-Type": "application/json",
      "Accept": "application/json",
      "X-SDK-Version": "2.0.0",
      "X-SDK-Language": "typescript",
    }};
    if (this.apiKey) {{
      headers["Authorization"] = `Bearer ${{this.apiKey}}`;
    }}

    let lastError: Error | undefined;
    for (let attempt = 0; attempt < this.maxRetries; attempt++) {{
      try {{
        const response = await fetch(url, {{
          method,
          headers,
          body: options.body !== undefined ? JSON.stringify(options.body) : undefined,
          signal: AbortSignal.timeout(this.timeout),
        }});

        if (!response.ok) {{
          const errorBody = await response.json().catch(() => ({{}}));
          if (response.status === 429) {{
            await new Promise(r => setTimeout(r, Math.pow(1.5, attempt) * 1000));
            continue;
          }}
          throw new APIError(response.status, errorBody.message ?? response.statusText, errorBody.details);
        }}

        const text = await response.text();
        return text ? JSON.parse(text) : ({{}}) as T;
      }} catch (err) {{
        lastError = err as Error;
        if (attempt < this.maxRetries - 1) {{
          await new Promise(r => setTimeout(r, Math.pow(1.5, attempt) * 500));
        }}
      }}
    }}
    throw lastError;
  }}

  protected get<T>(path: string, params?: Record<string, any>): Promise<T> {{
    return this.request<T>("GET", path, {{ params }});
  }}

  protected post<T>(path: string, body?: any, params?: Record<string, any>): Promise<T> {{
    return this.request<T>("POST", path, {{ body, params }});
  }}

  protected put<T>(path: string, body?: any): Promise<T> {{
    return this.request<T>("PUT", path, {{ body }});
  }}

  protected patch<T>(path: string, body?: any): Promise<T> {{
    return this.request<T>("PATCH", path, {{ body }});
  }}

  protected delete<T>(path: string): Promise<T> {{
    return this.request<T>("DELETE", path);
  }}
}}'''

    def _generate_api_classes(self) -> str:
        tag_operations: dict[str, list[dict]] = {}
        for path, path_item in self.paths.items():
            for method, operation in path_item.items():
                if not isinstance(operation, dict):
                    continue
                tags = operation.get("tags", ["Default"])
                for tag in tags:
                    tag_clean = tag.replace("/", "_").replace(" ", "")
                    if tag_clean not in tag_operations:
                        tag_operations[tag_clean] = []
                    tag_operations[tag_clean].append({
                        "path": path,
                        "method": method.upper(),
                        "operation": operation,
                    })

        lines = ["// API classes"]
        for tag, operations in tag_operations.items():
            class_name = f"{to_pascal_case(tag)}API"
            lines.append(f"export class {class_name} extends BaseClient {{")
            for op_info in operations:
                path = op_info["path"]
                http_method = op_info["method"]
                operation = op_info["operation"]
                op_id = operation.get("operationId", "")
                method_name = to_camel_case(op_id) if op_id else http_method.lower()
                summary = operation.get("summary", "")
                params = operation.get("parameters", [])
                has_body = bool(operation.get("requestBody"))
                path_params = [p for p in params if p.get("in") == "path"]
                query_params = [p for p in params if p.get("in") == "query"]

                args = []
                for p in path_params:
                    args.append(f"{to_camel_case(p['name'])}: string")
                if has_body:
                    args.append("body?: Record<string, any>")
                for p in query_params:
                    ts_type = get_ts_type(p.get("schema", {"type": "string"}), self.schemas)
                    optional = "" if p.get("required") else "?"
                    args.append(f"{to_camel_case(p['name'])}{optional}: {ts_type}")

                ts_path = re.sub(r"\{(\w+)\}", lambda m: "${" + to_camel_case(m.group(1)) + "}", path)
                lines.append(f"  /** {summary} */")
                lines.append(f"  async {method_name}({', '.join(args)}): Promise<any> {{")
                lines.append(f"    const path = `{ts_path}`;")

                if query_params:
                    qp_names = [to_camel_case(p["name"]) for p in query_params]
                    params_obj = ", ".join(f"{n}: {n}" for n in qp_names)
                    lines.append(f"    const params = {{ {params_obj} }};")

                if http_method == "GET":
                    params_arg = "params" if query_params else "undefined"
                    lines.append(f"    return this.get(path, {params_arg});")
                elif http_method == "POST":
                    lines.append("    return this.post(path, body);")
                elif http_method == "PUT":
                    lines.append("    return this.put(path, body);")
                elif http_method == "PATCH":
                    lines.append("    return this.patch(path, body);")
                elif http_method == "DELETE":
                    lines.append("    return this.delete(path);")
                lines.append("  }")
            lines.append("}")
            lines.append("")

        return "\n".join(lines)

    def _generate_exports(self) -> str:
        tag_names = set()
        for path_item in self.paths.values():
            for operation in path_item.values():
                if isinstance(operation, dict):
                    for tag in operation.get("tags", []):
                        tag_names.add(tag.replace("/", "_").replace(" ", ""))

        init_lines = []
        for tag in sorted(tag_names):
            class_name = f"{to_pascal_case(tag)}API"
            attr_name = to_camel_case(tag)
            init_lines.append(f"    this.{attr_name} = new {class_name}(config);")

        attrs = []
        for tag in sorted(tag_names):
            class_name = f"{to_pascal_case(tag)}API"
            attr_name = to_camel_case(tag)
            attrs.append(f"  readonly {attr_name}: {class_name};")

        return f'''// Main SintraPrime client
export class SintraPrimeClient {{
{chr(10).join(attrs)}

  constructor(config: ClientConfig = {{}}) {{
{chr(10).join(init_lines)}
  }}
}}

export default SintraPrimeClient;'''

    def save(self, output_path: str | Path) -> Path:
        code = self.generate()
        path = Path(output_path)
        path.parent.mkdir(parents=True, exist_ok=True)
        path.write_text(code)
        print(f"✅ TypeScript SDK saved: {path} ({len(code.splitlines())} lines)")
        return path


# ---------------------------------------------------------------------------
# Curl Example Generator
# ---------------------------------------------------------------------------

class CurlExampleGenerator:
    """Generates curl examples for every API endpoint."""

    def __init__(self, spec: dict):
        self.spec = spec
        self.paths = spec.get("paths", {})
        self.servers = spec.get("servers", [{}])
        self.base_url = self.servers[0].get("url", "https://api.example.com") if self.servers else ""

    def generate_all(self) -> list[dict]:
        """Generate curl examples for all endpoints."""
        examples = []
        for path, path_item in self.paths.items():
            for method, operation in path_item.items():
                if not isinstance(operation, dict):
                    continue
                example = self._generate_curl(path, method.upper(), operation)
                examples.append(example)
        return examples

    def _generate_curl(self, path: str, method: str, operation: dict) -> dict:
        params = operation.get("parameters", [])
        request_body = operation.get("requestBody", {})
        op_id = operation.get("operationId", "")
        summary = operation.get("summary", "")
        tags = operation.get("tags", [])

        # Build URL with example path params
        url = self.base_url + path
        for param in [p for p in params if p.get("in") == "path"]:
            example_val = param.get("schema", {}).get("example", f"example_{param['name']}")
            url = url.replace("{" + param["name"] + "}", str(example_val))

        # Add example query params
        query_params = [p for p in params if p.get("in") == "query" and p.get("required")]
        if query_params:
            qp_parts = []
            for p in query_params:
                example_val = p.get("schema", {}).get("example", f"example")
                qp_parts.append(f"{p['name']}={urllib.parse.quote(str(example_val))}")
            url += "?" + "&".join(qp_parts)

        # Build curl command
        lines = [f"# {summary}"]
        lines.append(f"curl -X {method} \\")
        lines.append(f'  "{url}" \\')
        lines.append('  -H "Authorization: Bearer YOUR_API_KEY" \\')
        lines.append('  -H "Content-Type: application/json"')

        if request_body and method in ("POST", "PUT", "PATCH"):
            example_body = self._get_example_body(request_body)
            if example_body:
                body_json = json.dumps(example_body, indent=2)
                # Indent the body for curl
                body_lines = body_json.splitlines()
                lines[-1] += " \\"
                lines.append(f"  -d '{body_lines[0]}")
                for line in body_lines[1:]:
                    lines.append(f"  {line}")
                lines[-1] += "'"

        return {
            "operation_id": op_id,
            "summary": summary,
            "method": method,
            "path": path,
            "tags": tags,
            "curl": "\n".join(lines),
        }

    def _get_example_body(self, request_body: dict) -> dict | None:
        """Extract or generate example request body."""
        content = request_body.get("content", {})
        json_content = content.get("application/json", {})
        if "example" in json_content:
            return json_content["example"]
        schema = json_content.get("schema", {})
        if "$ref" in schema:
            return {"example": "See schema for required fields"}
        return {"key": "value"}

    def save_markdown(self, output_path: str | Path) -> Path:
        """Save curl examples as markdown file."""
        examples = self.generate_all()
        lines = ["# API Curl Examples", "", f"Generated {len(examples)} examples.", ""]

        by_tag: dict[str, list[dict]] = {}
        for ex in examples:
            tags = ex["tags"] or ["Other"]
            for tag in tags:
                by_tag.setdefault(tag, []).append(ex)

        for tag, tag_examples in sorted(by_tag.items()):
            lines.append(f"## {tag}")
            lines.append("")
            for ex in tag_examples:
                lines.append(f"### {ex['summary']}")
                lines.append(f"`{ex['method']} {ex['path']}`")
                lines.append("")
                lines.append("```bash")
                lines.append(ex["curl"])
                lines.append("```")
                lines.append("")

        content = "\n".join(lines)
        path = Path(output_path)
        path.parent.mkdir(parents=True, exist_ok=True)
        path.write_text(content)
        print(f"✅ Curl examples saved: {path} ({len(examples)} endpoints)")
        return path


