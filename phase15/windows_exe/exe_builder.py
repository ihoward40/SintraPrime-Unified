# NSIS installer script generator
# ---------------------------------------------------------------------------

class NSISGenerator:
    """Generates a NSIS installer script (.nsi) for Windows."""

    def __init__(self, config: ExeConfig):
        self.config = config

    def generate(self) -> str:
        c = self.config
        auto_start_section = ""
        if c.auto_start:
            auto_start_section = textwrap.dedent(f"""
            Section "Auto-start on login"
              WriteRegStr HKCU "Software\\Microsoft\\Windows\\CurrentVersion\\Run" \\
                "{c.app_name}" "$INSTDIR\\{c.app_name}.exe"
            SectionEnd
            """)

        return textwrap.dedent(f"""\
            ; NSIS Installer Script for {c.app_name}
            ; Auto-generated by SintraPrime ExeBuilder v{c.app_version}

            !include "MUI2.nsh"
            !include "FileFunc.nsh"

            Name "{c.app_name}"
            OutFile "{c.nsis_output_dir}\\{c.app_name}_Setup_v{c.app_version}.exe"
            InstallDir "$PROGRAMFILES64\\{c.company_name}\\{c.app_name}"
            InstallDirRegKey HKLM "Software\\{c.company_name}\\{c.app_name}" "InstallDir"
            RequestExecutionLevel admin

            ; MUI Settings
            !define MUI_ABORTWARNING
            !define MUI_ICON "{c.icon_path}"
            !define MUI_UNICON "{c.icon_path}"
            !define MUI_WELCOMEPAGE_TITLE "Welcome to {c.app_name} Setup"
            !define MUI_WELCOMEPAGE_TEXT "This wizard will install {c.app_name} v{c.app_version} on your computer.$\\n$\\n{c.app_description}"

            ; Pages
            !insertmacro MUI_PAGE_WELCOME
            !insertmacro MUI_PAGE_LICENSE "LICENSE.txt"
            !insertmacro MUI_PAGE_DIRECTORY
            !insertmacro MUI_PAGE_INSTFILES
            !insertmacro MUI_PAGE_FINISH

            !insertmacro MUI_UNPAGE_CONFIRM
            !insertmacro MUI_UNPAGE_INSTFILES

            !insertmacro MUI_LANGUAGE "English"

            ; Version Information
            VIProductVersion "{c.app_version}.0"
            VIAddVersionKey "ProductName" "{c.app_name}"
            VIAddVersionKey "CompanyName" "{c.company_name}"
            VIAddVersionKey "FileDescription" "{c.app_description} Installer"
            VIAddVersionKey "FileVersion" "{c.app_version}"

            Section "Main Application" SEC01
              SetOutPath "$INSTDIR"
              File "{c.output_dir}\\{c.app_name}.exe"
              File /r "assets"

              ; Create shortcuts
              CreateDirectory "$SMPROGRAMS\\{c.company_name}"
              CreateShortCut "$SMPROGRAMS\\{c.company_name}\\{c.app_name}.lnk" "$INSTDIR\\{c.app_name}.exe"
              CreateShortCut "$DESKTOP\\{c.app_name}.lnk" "$INSTDIR\\{c.app_name}.exe"

              ; Registry entries
              WriteRegStr HKLM "Software\\{c.company_name}\\{c.app_name}" "InstallDir" "$INSTDIR"
              WriteRegStr HKLM "Software\\{c.company_name}\\{c.app_name}" "Version" "{c.app_version}"

              ; Add/Remove Programs entry
              WriteRegStr HKLM "Software\\Microsoft\\Windows\\CurrentVersion\\Uninstall\\{c.app_name}" \\
                "DisplayName" "{c.app_name}"
              WriteRegStr HKLM "Software\\Microsoft\\Windows\\CurrentVersion\\Uninstall\\{c.app_name}" \\
                "UninstallString" "$INSTDIR\\Uninstall.exe"
              WriteRegStr HKLM "Software\\Microsoft\\Windows\\CurrentVersion\\Uninstall\\{c.app_name}" \\
                "DisplayVersion" "{c.app_version}"
              WriteRegStr HKLM "Software\\Microsoft\\Windows\\CurrentVersion\\Uninstall\\{c.app_name}" \\
                "Publisher" "{c.company_name}"
              WriteRegDWORD HKLM "Software\\Microsoft\\Windows\\CurrentVersion\\Uninstall\\{c.app_name}" \\
                "NoModify" 1
              WriteRegDWORD HKLM "Software\\Microsoft\\Windows\\CurrentVersion\\Uninstall\\{c.app_name}" \\
                "NoRepair" 1

              WriteUninstaller "$INSTDIR\\Uninstall.exe"
            SectionEnd
            {auto_start_section}
            Section "Uninstall"
              Delete "$INSTDIR\\{c.app_name}.exe"
              Delete "$INSTDIR\\Uninstall.exe"
              RMDir /r "$INSTDIR\\assets"
              RMDir "$INSTDIR"
              Delete "$SMPROGRAMS\\{c.company_name}\\{c.app_name}.lnk"
              RMDir "$SMPROGRAMS\\{c.company_name}"
              Delete "$DESKTOP\\{c.app_name}.lnk"
              DeleteRegKey HKLM "Software\\{c.company_name}\\{c.app_name}"
              DeleteRegKey HKLM "Software\\Microsoft\\Windows\\CurrentVersion\\Uninstall\\{c.app_name}"
            SectionEnd
        """)


# ---------------------------------------------------------------------------
# Tray icon integration (pystray-based)
# ---------------------------------------------------------------------------

class TrayIconGenerator:
    """Generates the Windows system tray icon integration script."""

    def __init__(self, config: ExeConfig):
        self.config = config

    def generate(self) -> str:
        c = self.config
        return textwrap.dedent(f"""\
            \"\"\"
            SintraPrime Windows Taskbar Tray Icon
            Auto-generated by ExeBuilder v{c.app_version}
            \"\"\"
            import threading
            import webbrowser
            import sys
            import os

            try:
                import pystray
                from PIL import Image
                TRAY_AVAILABLE = True
            except ImportError:
                TRAY_AVAILABLE = False

            SINTRA_URL = "http://localhost:{c.port}"
            ICON_PATH = os.path.join(os.path.dirname(__file__), "assets", "sintra_icon.ico")


            def _open_dashboard(_icon=None, _item=None):
                webbrowser.open(SINTRA_URL)


            def _open_settings(_icon=None, _item=None):
                webbrowser.open(f"{{SINTRA_URL}}/settings")


            def _quit_app(icon, _item=None):
                icon.stop()
                sys.exit(0)


            def _load_icon():
                try:
                    return Image.open(ICON_PATH)
                except Exception:
                    # Fallback: create a simple 64x64 blue square
                    img = Image.new("RGB", (64, 64), color=(30, 100, 200))
                    return img


            def run_tray():
                if not TRAY_AVAILABLE:
                    return
                icon = pystray.Icon(
                    name="{c.app_name}",
                    icon=_load_icon(),
                    title="{c.app_name} — {c.app_description}",
                    menu=pystray.Menu(
                        pystray.MenuItem("Open Dashboard", _open_dashboard, default=True),
                        pystray.MenuItem("Settings", _open_settings),
                        pystray.Menu.SEPARATOR,
                        pystray.MenuItem("Quit {c.app_name}", _quit_app),
                    ),
                )
                icon.run()


            def start_tray_in_background():
                t = threading.Thread(target=run_tray, daemon=True)
                t.start()
                return t


            if __name__ == "__main__":
                run_tray()
        """)


