class InstallerType(str, Enum):
    NSIS = "nsis"
    WIX = "wix"
    INNO = "inno"


class DeploymentTarget(str, Enum):
    SINGLE_USER = "single_user"
    ALL_USERS = "all_users"
    PORTABLE = "portable"


@dataclass
class AppConfig:
    name: str = "SintraPrime"
    version: str = "1.0.0"
    author: str = "SintraPrime AI"
    description: str = "Autonomous Legal AI Platform"
    icon_path: str = "assets/sintra_icon.ico"
    entry_point: str = "sintra_main.py"
    install_dir: str = r"C:\Program Files\SintraPrime"
    data_dir: str = r"%APPDATA%\SintraPrime"
    python_version: str = "3.11"
    target: DeploymentTarget = DeploymentTarget.ALL_USERS
    installer_type: InstallerType = InstallerType.NSIS
    enable_auto_update: bool = True
    enable_system_tray: bool = True
    enable_startup: bool = False


@dataclass
class DeploymentArtifact:
    artifact_id: str
    artifact_type: str
    filename: str
    content: str
    description: str
    size_bytes: int = 0

    def __post_init__(self):
        self.size_bytes = len(self.content.encode("utf-8"))


@dataclass
class DeploymentBundle:
    bundle_id: str
    app_config: AppConfig
    artifacts: List[DeploymentArtifact] = field(default_factory=list)
    created_at: float = field(default_factory=lambda: __import__("time").time())

    def get_artifact(self, artifact_type: str) -> Optional[DeploymentArtifact]:
        for a in self.artifacts:
            if a.artifact_type == artifact_type:
                return a
        return None

    def artifact_types(self) -> List[str]:
        return [a.artifact_type for a in self.artifacts]

    def total_size_bytes(self) -> int:
        return sum(a.size_bytes for a in self.artifacts)

    def save_all(self, output_dir: str) -> List[str]:
        """Write all artifacts to disk and return file paths."""
        out = Path(output_dir)
        out.mkdir(parents=True, exist_ok=True)
        paths = []
        for artifact in self.artifacts:
            p = out / artifact.filename
            p.write_text(artifact.content, encoding="utf-8")
            paths.append(str(p))
        return paths


